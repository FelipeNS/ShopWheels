    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        // TODO add your handling code here:
        
        // Declarando variaveis
        String nome = txtNome.getText();
        
        // Salvando na tabela do BD
        if (rootPaneCheckingEnabled) {
        
        // Conectando no banco
        try {
            Connection conexaoDB = ConexaoDB.abrir();
            
        // Executando comando MySQL
            CallableStatement comandoCli = conexaoDB.prepareCall("INSERT INTO cliente (cli_nome) values (?)");
            comandoCli.setString(1, nome);
            comandoCli.execute();
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Botão Salvar " + e.getMessage());
        }
        } else {
            JOptionPane.showMessageDialog(null, "Preencha todos os campos!");
        }
        
        txtNome.setText("");
        
    }//GEN-LAST:event_btnSalvarActionPerformed

