public void entrar(){    

    String login = txtLogin.getText();
    String senha = pwdSenha.getText();
    String loginDB = "";
    String senhaDB = "";
            
    // Conectando no Banco    
        try {
            Connection conexao = ConexaoDB.abrir();
                
        // Executando comando MySQL
        // Pegar login
            Statement comandoFun = conexao.createStatement();
            ResultSet resultadoFun = comandoFun.executeQuery("SELECT log_login, log_senha, log_fk_funcionario FROM login ORDER BY log_login");
            
        // Inserindo itens    
            while(resultadoFun.next()) {
              
             String loginBD = resultadoFun.getString("log_login");
             String senhaBD = resultadoFun.getString("log_senha");
             String fkFun = resultadoFun.getString("log_fk_funcionario");
                
                if (loginBD.equals(login) && senhaBD.equals(senha) && "1".equals(fkFun)) {
                    loginDB = loginBD;
                    senhaDB = senhaBD;
                        
                // Pegando Nome e Id do funcionario    
                    Statement comando = conexao.createStatement();
                    ResultSet resultado = comando.executeQuery("SELECT fun_nome, log_fk_funcionario FROM login INNER JOIN funcionario ON log_fk_funcionario=fun_id WHERE log_login='" + loginDB + "' AND log_senha='" + senhaDB + "'");
                // Guardando nome e Id do funcionario
                    while (resultado.next()) {
                    Login n1 = new Login();
                    n1.setNome(resultado.getString("fun_nome"));
                                
                    n1.setId(resultado.getInt("log_fk_funcionario"));   
                            
                    }
                // Abrindo tela    
                    TelaPrincipalADM abrir = new TelaPrincipalADM();
                    abrir.setExtendedState(MAXIMIZED_BOTH);
                    abrir.show();
                    this.dispose();
                        
                        
                }else{
                    if (loginBD.equals(login) && senhaBD.equals(senha) && !"1".equals(loginBD)) {
                        loginDB = loginBD;
                        senhaDB = senhaBD;
                           
                    // Pegando Nome e Id do funcionario    
                        Statement comando2 = conexao.createStatement();
                        ResultSet resultado2 = comando2.executeQuery("SELECT fun_nome, log_fk_funcionario FROM login INNER JOIN funcionario ON log_fk_funcionario=fun_id WHERE log_login='" + loginDB + "' AND log_senha='" + senhaDB + "'");
                    // Guardando nome e Id do funcionario
                        while (resultado2.next()) {
                            Login n2 = new Login();
                            n2.setNome(resultado2.getString("fun_nome"));
                            
                            n2.setId(resultado2.getInt("log_fk_funcionario"));   
                            
                        }
                            
                    // Abrindo tela    
                        TelaPrincipalFuncionario abrir = new TelaPrincipalFuncionario();
                        abrir.setExtendedState(MAXIMIZED_BOTH);
                        abrir.show();
                        this.dispose();
                        
                    }
  
                }
                    
                }
                
            if (!login.equals(loginDB) || !senha.equals(senhaDB)) {
                JOptionPane.showMessageDialog(null, "Login ou senha Inválidos");
                txtLogin.setText("");
                pwdSenha.setText("");
            }
                
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Botão Entrar " +e.getMessage());
        }
    
}
    
    
