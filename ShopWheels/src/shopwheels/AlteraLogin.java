    private void formComponentShown(java.awt.event.ComponentEvent evt) {//GEN-FIRST:event_formComponentShown
        // TODO add your handling code here:
        
        // Colocando valores no ComboBox
            
        // Conectando no Banco    
            try {
                Connection conexao = ConexaoDB.abrir();
                
            // Executando comando MySQL
                Statement comando = conexao.createStatement();
                ResultSet resultado = comando.executeQuery("SELECT fun_id, fun_nome FROM funcionario ORDER BY fun_nome");
            
            // Inserindo itens    
                while (resultado.next()) {
                    int id = resultado.getInt("fun_id");
                    String nome = resultado.getString("fun_nome");
                    cmbFuncionario.addItem(id + "-" + nome);
                }
            
            } catch (Exception e) {
        JOptionPane.showMessageDialog(null, "Combo Component Show " +e.getMessage());
        }
            
        // Atualizar tabela
            atualizarTabela();
         
        // Desabilitar campos
            desabilitar();
            
        // Desabilitar botões
            btnCancelar.setEnabled(false);
            btnSalvar.setEnabled(false);
               
    }//GEN-LAST:event_formComponentShown

    private void btnAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAlterarActionPerformed
        // TODO add your handling code here:
        
        // Pegando dados da tabela
            if (tblLogin.getRowCount() > 0) {
                int linhaSelecionada = tblLogin.getSelectedRow();
            
            // Guardando valores pegos
                String id = tblLogin.getValueAt(linhaSelecionada, 0).toString();
                String login = tblLogin.getValueAt(linhaSelecionada, 1).toString();
                String senha = tblLogin.getValueAt(linhaSelecionada, 2).toString();
                String funcionario = tblLogin.getValueAt(linhaSelecionada, 3).toString();
                
            // Pegando id do funcionario    
            // Conectando no Banco
                try {
                    Connection conexao = ConexaoDB.abrir();
                    
                // Executando comando MySQL    
                    Statement comando = conexao.createStatement();
                    ResultSet resultado = comando.executeQuery("SELECT fun_id, fun_nome FROM funcionario ORDER BY fun_nome");
                
                // Colocando resultado em variáveis    
                    while (resultado.next()) {
                        int idFun = resultado.getInt("fun_id");
                        String nome = resultado.getString("fun_nome");
                
                // Verificando igualdade entre resultados        
                    if (nome.equals(funcionario)) {
                        String nomeCli;
                        nomeCli = (idFun + "-" + funcionario) ;
                
                    // Colocando item na Combo Box    
                        cmbFuncionario.setSelectedItem(nomeCli);
                        
                    }
                }
            
                } catch (Exception e) {
                    JOptionPane.showMessageDialog(null, "Botão Alterar " +e.getMessage());
                }

            // Colocando informações nos campos    
                txtID.setText(id);
                txtLogin.setText(login);
                txtSenha.setText(senha);
                
            // Habilitar campos para editar   
                habilitar();
                
            // Habilitar botões
                btnCancelar.setEnabled(true);
                btnSalvar.setEnabled(true);
            
            // Desabilitar botões
                btnExcluir.setEnabled(false);
                btnAlterar.setEnabled(false);
        
        }
                             
    }//GEN-LAST:event_btnAlterarActionPerformed

    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        // TODO add your handling code here:
        
        if (tblLogin.getRowCount() > 0) {
            if (JOptionPane.showConfirmDialog(null, "Deseja realmente excluir")== JOptionPane.OK_OPTION) {
                int linhaSelecionada = tblLogin.getSelectedRow();
                String id = tblLogin.getValueAt(linhaSelecionada, 0).toString();
            
        // Conectando no Banco        
            try {
                Connection conexao = ConexaoDB.abrir();
                
            // Executando comando MySQL    
                CallableStatement comando = conexao.prepareCall("DELETE FROM login WHERE log_id=?");
                comando.setInt(1, Integer.parseInt(id));
                comando.execute();
                
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, "Botao excluir " +e.getMessage());
            
            }
            
        }
        }
        
        // Atualizar tabela
            atualizarTabela();
         
    }//GEN-LAST:event_btnExcluirActionPerformed

    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        // TODO add your handling code here:
        
        // Declarando variáveis
            String id = txtID.getText();
            String login = txtLogin.getText();
            String senha = txtSenha.getText();
            String funcionario = cmbFuncionario.getSelectedItem().toString();
        
        // Pegando id do cliente    
            String[] pegaIDFun = funcionario.split("-");
            int idFun = Integer.parseInt(pegaIDFun[0]);
            
        
        // Salvando na tabela do BD
            if (!"".equals(login) && !"".equals(senha)){
        
        // Conectando no banco    
            try {
                Connection conexao = ConexaoDB.abrir();
            
            // Executando comando MySQL
                CallableStatement comandoLog = conexao.prepareCall("UPDATE login SET log_login=?, log_senha=?, log_fk_funcionario=? WHERE log_id=?");
                comandoLog.setString(1, login);
                comandoLog.setString(2, senha);
                comandoLog.setInt(3, idFun);
                comandoLog.setString(4, id);
                comandoLog.execute();
                
                JOptionPane.showMessageDialog(null, "Alterado com sucesso");
        
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null,"Botão salvar" + e.getMessage());
            
            }
            }else{
                JOptionPane.showMessageDialog(null, "Prencha todos os campos!");   
            }
        
    // Limpar campos    
        limpar();
    
    // Desabilitar campos
        desabilitar();
    
    // Desabilitar botões
        btnSalvar.setEnabled(false);
        btnCancelar.setEnabled(false);
    
    // Habilitar botões
        btnExcluir.setEnabled(true);
        btnAlterar.setEnabled(true);
    
    // Atualizar tabela
        atualizarTabela();
        
    }//GEN-LAST:event_btnSalvarActionPerformed

