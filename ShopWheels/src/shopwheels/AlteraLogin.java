public void atualizarTabela(){
    
    // Definindo modelo da tabela
        DefaultTableModel modelo = new DefaultTableModel();
        modelo.addColumn("ID");
        modelo.addColumn("LOGIN");
        modelo.addColumn("SENHA");
        modelo.addColumn("FUNCIONÁRIO");
        tblLogin.setModel(modelo);
       
    // Conectando com o DB
        try {
            Connection conexaoDB = ConexaoDB.abrir();
        
        // Executando comando MySQL    
            Statement comandoFun = conexaoDB.createStatement();
            ResultSet resultadoTbl = comandoFun.executeQuery("SELECT log_id, fun_nome, log_login, log_senha FROM login INNER JOIN funcionario ON log_fk_funcionario=fun_id");
        
        // Setando valores nas linhas da tabela    
            while (resultadoTbl.next()) {
                Object[] linha = new Object[4];
                linha[0] = resultadoTbl.getInt("log_id");
                linha[1] = resultadoTbl.getString("log_login");
                linha[2] = resultadoTbl.getString("log_senha");
                linha[3] = resultadoTbl.getString("fun_nome");
                modelo.insertRow(tblLogin.getRowCount(), linha);
            }
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Atualizar tabela" +e.getMessage());
        } 
        
}    


